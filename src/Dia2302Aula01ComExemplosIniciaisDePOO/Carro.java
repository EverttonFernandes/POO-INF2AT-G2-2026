package Dia2302Aula01ComExemplosIniciaisDePOO;

public class Carro {
    String placa;
    String cor;
    String modelo;
    int anoDeFabricacao;

    public Carro(String placa, String cor, String modelo, int anoDeFabricacao) {
        this.placa = placa;
        this.cor = cor;
        this.modelo = modelo;
        this.anoDeFabricacao = anoDeFabricacao;
    }
}
